/* operadores artimeticos


+ -> adição
- -> subtração
* -> multiplicação
/ -> divisão
% -> resto da divisão
++ -> incremento
-- -> decremento
Exemplo:
let a = 10;
let b = 5;
let soma = a + b; // 15
let subtracao = a - b; // 5
let multiplicacao = a * b; // 50
let divisao = a / b; // 2
let resto = a % b; // 0
a++; // 11
a--;
// 10
b++; // 6
b--; // 5
 */


const result=2

